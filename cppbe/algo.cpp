float cor(fftwf_complex *af, size_t afsz, uint8_t pixData[512]) {
    float diffs[512];
    fftwf_complex out[512];

    diffs[0]   = 0;
    diffs[511] = 0;

    for(size_t i = 1; i < 511; i++) {
        float delta = (pixData[i + 1] - pixData[i - 1]) / 2.0f;
        diffs[i-1] = delta > 1.0f ? 1.0f : (delta < -1.0f ? -1.0f : delta);
    }

    fftwf_complex cDiff[512];
    for(size_t i = 0; i < 512; i++) {
        cDiff[i][0] = diffs[i];
        cDiff[i][1] = 0;
    }

    fftwf_plan planf = fftwf_plan_dft_1d(512, cDiff, out, FFTW_FORWARD, FFTW_ESTIMATE);
    fftwf_execute(planf);

    for(size_t i = 0; i < afsz; i++) {
        out[i][0] *= af[i][0];
        out[i][1] *= af[i][1];
    }

    fftwf_plan planb = fftwf_plan_dft_1d(512, out, cDiff, FFTW_BACKWARD, FFTW_ESTIMATE);
    fftwf_execute(planb);

    float max = -INFINITY;

    for (float *diff : cDiff) {
        if(diff[0] > max) { max = diff[0]; }
    }

    return max;
}

void precompute(uint8_t inp[512], fftwf_complex out[512]) {
    float diffs[512];

    diffs[0]   = 0;
    diffs[511] = 0;

    for(size_t i = 1; i < 511; i++) {
        float delta = (inp[i + 1] - inp[i - 1]) / 2.0f;
        //diffs[i] = delta > 1.0f ? 1.0f : (delta < -1.0f ? -1.0f : delta);
        diffs[i] = delta;
    }

    fftwf_complex cDiff[512];
    for(size_t i = 0; i < 512; i++) {
        cDiff[i][0] = diffs[i];
        cDiff[i][1] = 0;
    }

    fftwf_plan planf = fftwf_plan_dft_1d(510, cDiff, out, FFTW_FORWARD, FFTW_ESTIMATE);
    fftwf_execute(planf);

    for(size_t i = 0; i < 511; i++) {
        out[i][1] *= -1;
    }
}

